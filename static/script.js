const chatBody = document.getElementById('chatBody');
const userInput = document.getElementById('userInput');
const sendBtn = document.getElementById('sendBtn');

function scrollToBottom(){
  chatBody.scrollTop = chatBody.scrollHeight;
}

function addUserMessage(text){
  const row = document.createElement('div');
  row.className = 'msg-row user';
  row.innerHTML = `<div class="bubble"></div>`;
  row.querySelector('.bubble').textContent = text;
  chatBody.appendChild(row);
  scrollToBottom();
}

function addBotMessageInstant(html){
  const row = document.createElement('div');
  row.className = 'msg-row bot';
  row.innerHTML = `<div class="msg-avatar">H30</div><div class="bubble">${html}</div>`;
  chatBody.appendChild(row);
  scrollToBottom();
}

function showTyping(){
  const row = document.createElement('div');
  row.className = 'msg-row bot';
  row.id = 'typingRow';
  row.innerHTML = `<div class="msg-avatar">H30</div>
    <div class="bubble typing-bubble">
      <span class="typing-dot"></span><span class="typing-dot"></span><span class="typing-dot"></span>
    </div>`;
  chatBody.appendChild(row);
  scrollToBottom();
}

function removeTyping(){
  const row = document.getElementById('typingRow');
  if(row) row.remove();
}

function renderChips(list, onClick){
  const wrap = document.createElement('div');
  wrap.className = 'quick-replies';
  list.forEach(item=>{
    const chip = document.createElement('button');
    chip.className = 'chip';
    chip.textContent = item.label;
    chip.onclick = ()=> onClick(item);
    wrap.appendChild(chip);
  });
  chatBody.appendChild(wrap);
  scrollToBottom();
  return wrap;
}

function clearOldChips(){
  document.querySelectorAll('.quick-replies').forEach(el=>el.remove());
}

function botReply(html, delay){
  showTyping();
  const typingTime = delay || Math.min(1600, 500 + html.length * 4);
  setTimeout(()=>{
    removeTyping();
    addBotMessageInstant(html);
    setTimeout(showMainMenu, 350);
  }, typingTime);
}

function showMainMenu(){
  clearOldChips();
  renderChips(MAIN_MENU, handleMenuClick);
}

function showSssMenu(){
  clearOldChips();
  addBotMessageInstant("En çok sorulan konulardan birini seçebilir veya sorunuzu doğrudan yazabilirsiniz 👇");
  renderChips(SSS_MENU, (item)=>{
    clearOldChips();
    addUserMessage(item.label);
    botReply(KB[item.key]);
  });
  scrollToBottom();
}

function handleMenuClick(item){
  clearOldChips();
  addUserMessage(item.label);

  if(item.key === 'sss_menu'){
    showTyping();
    setTimeout(()=>{
      removeTyping();
      showSssMenu();
    }, 700);
    return;
  }
  botReply(KB[item.key]);
}

function handleFreeText(text){
  clearOldChips();
  addUserMessage(text);
  const matchKey = searchKB(text);

  if(matchKey){
    botReply(KB[matchKey]);
  } else {
    botReply(`Bu konuda net bir bilgi bulamadım 🙏 Size en doğru bilgiyi verebilmesi için doğrudan HIT-30 Destek Ofisi ile iletişime geçmenizi öneririm:<br><br>
    📧 <b>hit30@sanayi.gov.tr</b><br>
    🌐 <a href="https://www.yatirimadestek.gov.tr" target="_blank">yatirimadestek.gov.tr</a> üzerinden Teşvik Robotu'nu da deneyebilirsiniz.<br><br>
    Aşağıdan başka bir konu seçebilirsiniz 👇`, 1200);
  }
}

sendBtn.addEventListener('click', ()=>{
  const text = userInput.value.trim();
  if(!text) return;
  handleFreeText(text);
  userInput.value = '';
});

userInput.addEventListener('keydown', (e)=>{
  if(e.key === 'Enter'){
    e.preventDefault();
    sendBtn.click();
  }
});

